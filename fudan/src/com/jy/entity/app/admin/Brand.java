package com.jy.entity.app.admin;

public class Brand {
	
	private int brand_id;                                            
	private String brand_name;                          
	private String brand_class;                                    
	private String brand_pic;     
	private int brand_recommend;
	private int brand_apply;
	private int class_id;
	
	public int getBrand_id() {
		return brand_id;
	}
	public void setBrand_id(int brand_id) {
		this.brand_id = brand_id;
	}
	public String getBrand_name() {
		return brand_name;
	}
	public void setBrand_name(String brand_name) {
		this.brand_name = brand_name;
	}
	public String getBrand_class() {
		return brand_class;
	}
	public void setBrand_class(String brand_class) {
		this.brand_class = brand_class;
	}
	public String getBrand_pic() {
		return brand_pic;
	}
	public void setBrand_pic(String brand_pic) {
		this.brand_pic = brand_pic;
	}
	public int getBrand_recommend() {
		return brand_recommend;
	}
	public void setBrand_recommend(int brand_recommend) {
		this.brand_recommend = brand_recommend;
	}
	public int getBrand_apply() {
		return brand_apply;
	}
	public void setBrand_apply(int brand_apply) {
		this.brand_apply = brand_apply;
	}
	public int getClass_id() {
		return class_id;
	}
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}
	
}

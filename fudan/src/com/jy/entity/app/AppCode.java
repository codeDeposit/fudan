package com.jy.entity.app;

public class AppCode {
	
	private int id;
	private String code;
	private Long send_code_time;
	private String phone;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Long getSend_code_time() {
		return send_code_time;
	}
	public void setSend_code_time(Long send_code_time) {
		this.send_code_time = send_code_time;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
}
